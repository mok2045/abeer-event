# تحوّل ون — ملخص المشروع الكامل

## الجوهر
شركة ناشئة سعودية تبني وكلاء ذكاء اصطناعي عربية اللغة تعمل على واتساب، تستهدف المنشآت الصغيرة (SME) لأتمتة الرد على الاستفسارات والحجوزات والطلبات ٢٤/٧.

## الملاحظة الأصلية
المنشآت الصغيرة في السعودية تغرق في رسائل واتساب — استفسارات وطلبات وحجوزات — وصاحب المحل يفوته عملاء لأنه لا يستطيع الرد في الوقت المناسب. الحل: وكيل ذكاء اصطناعي يرد بالعربية الفصيحة والعامية السعودية على مدار الساعة بتكلفة أقل من 0.05 ريال للمحادثة.

## الصياغات الثلاث (مشروع واحد، ثلاثة نماذج أعمال مختلفة)

### الصياغة 1: سوق B2B2B (السوق الثلاثي)
- **النموذج:** منصة وسيطة تربط مطورين خارجيين بمنشآت عميلة
- **تقسيم الإيراد:** 70% للمطور / 30% للمنصة
- **السعر للعميل:** 400–5,000 ريال/شهر
- **المشكلة الحادة:** دجاجة وبيضة — بدون مطورين لا عملاء، وبدون عملاء لا مطورين
- **خطر إضافي:** 90% من الأسواق الثلاثية تفشل تاريخياً

### الصياغة 2: بنية PaaS (مزود البنية التحتية)
- **النموذج:** أنت توفر كل الأدوات جاهزة (واتساب+LLM+RAG+عزل) والمطوّر يبني المنطق فقط
- **الإيراد:** تكديس هوامش على كل طبقة (واتساب + LLM + اشتراك)
- **المشكلة:** تحتاج رأسمال وعمق هندسي أكبر + Unifonic قادر على نسخها بسرعة
- **تهديد:** Unifonic (ممولة بـ SoftBank) = "الفيل في الغرفة"

### الصياغة 3: SaaS عمودي (المنتج المباشر)
- **النموذج:** المؤسس وفريقه يبنون وكلاء متخصصة لقطاع واحد (البداية بمحلات الورد)
- **السعر:** 249–399 ريال/شهر
- **الفريق:** مصر تقنياً + الرياض للإدارة والمبيعات
- **الميزة:** لا مشكلة دجاجة وبيضة، مسار مالي واضح
- **الضعف:** سقف سوقي محدود (~3,000 محل ورد)، خندق رفيع

## الأرقام الاقتصادية الأساسية
- تكلفة المحادثة عبر Haiku 4.5: أقل من 0.05 ريال
- رسائل الخدمة الواردة من Meta: مجانية
- التكلفة المهيمنة: واتساب Business API (وليس LLM)
- الهامش الإجمالي: 60–90% حسب الصياغة
- LTV:CAC المتوقع في الصياغة 3: 8:1 إلى 31:1
- نقطة التعادل في الصياغة 3: 109–230 عميل

## الكود الحالي
- 10,860 سطر مكتمل، تقييم 7.5/10 تصميماً، 5/10 جاهزية للإنتاج
- لم يُختبر مع عميل واحد حقيقي بعد — لا إيراد فعلي حتى اللحظة
- إحصاءات صفحة الهبوط وهمية (500 عميل قبل الإطلاق) — تضليل ذاتي يجب تصحيحه

## المنافسون الرئيسيون
